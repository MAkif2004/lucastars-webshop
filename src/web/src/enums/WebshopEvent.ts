/**
 * Enumeration of all webshop events
 */
export enum WebshopEvent {
    /** Represents the Welcome event */
    Welcome = "welcome",
    /** Represents the Game List filter event */
    Filter = "filter",
    /** Represents the Game List sort event */
    Sort = "sort",
    /** Represents the Game List page loaded event */
    GamesListLoaded = "gamesListLoaded",
    /** Represents the Item added to cart event */
    AddToCart = "addToCart",
};
